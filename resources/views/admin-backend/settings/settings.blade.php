                down_percentage: $("#down_percentage").val(),
                visibility_level: selectedVisibilityLevel
            };

            console.log(data);

            $.ajaxSetup({
                headers: {
                    'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                }
            });
            $.ajax({
                url: "{{ route('admin.category.storeCategory') }}",
                type: "POST",
                data: data,
                success: function(response) {
                    if (response) {
                        window.location.href = '{{ route("admin.settings") }}';
                    }
                }
            });
        });

        $("#category, #sub_category, #ticker, #exchange, #visibility_levels_drop").on("change", function () {

            if ($('#description').val() === '' && $('#ast').val() === '' && $('#up_percentage').val() === '' && $('#down_percentage').val() === ''){
                let data = {
                    html: ace.edit("html-editor").getSession().getValue(),
                    css: ace.edit("css-editor").getSession().getValue(),
                    js: ace.edit("js-editor").getSession().getValue(),
                    category: $("#category").val(),
                    sub_category: $("#sub_category").val(),
                    ticker: $("#ticker").val(),
                    exchange: $("#exchange").val(),
                    visibility_levels_drop: $("#visibility_levels_drop").val(),
                    _token: '{{ csrf_token() }}'
                };

                $.ajax({
                    url: "{{ route('admin.category.fetchCategory') }}",
                    method: 'POST',
                    data: data,
                    success: function (data) {
                        $("#description").val(data.description);
                        $("#ast").val(data.associated_search_terms);
                        $("#up_percentage").val(data.up_percentage);
                        $("#down_percentage").val(data.down_percentage);
                        ace.edit("html-editor").getSession().setValue(data.html_data);
                        ace.edit("css-editor").getSession().setValue(data.css_data);
                        ace.edit("js-editor").getSession().setValue(data.js_data);
                    },
                    error: function (xhr, textStatus, errorThrown) {
                        console.error(errorThrown);
                    }
                });
            }

